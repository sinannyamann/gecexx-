import express from 'express';
import helmet from 'helmet';
import compression from 'compression';
import cors from 'cors';
import rateLimit from 'express-rate-limit';
import { WebSocketServer } from 'ws';
import { EventEmitter } from 'events';

// Geli≈ümi≈ü AI Sistemi
class PersonalAI extends EventEmitter {
  constructor() {
    super();
    this.memory = new Map(); // Kalƒ±cƒ± hafƒ±za
    this.userProfiles = new Map(); // Kullanƒ±cƒ± profilleri
    this.conversationHistory = new Map(); // Konu≈üma ge√ßmi≈üi
    this.learningData = []; // √ñƒürenme verisi
    this.personality = {
      responseStyle: 'friendly',
      learningRate: 0.1,
      memoryCapacity: 1000
    };
    this.selfModificationLog = [];
    this.startTime = Date.now();
  }

  // Geli≈ümi≈ü mesaj i≈üleme
  async processMessage(message, userId = 'default', context = {}) {
    try {
      // Kullanƒ±cƒ± profili g√ºncelle
      this.updateUserProfile(userId, message);
      
      // Konu≈üma ge√ßmi≈üine ekle
      this.addToHistory(userId, message);
      
      // Mesaj analizi
      const analysis = this.analyzeMessage(message);
      
      // Yanƒ±t √ºret
      const response = await this.generateResponse(message, userId, analysis, context);
      
      // √ñƒürenme verisi kaydet
      this.recordLearning(userId, message, response, analysis);
      
      // Kendini geli≈ütirme kontrol√º
      if (this.shouldSelfImprove()) {
        await this.improveSelf();
      }
      
      return {
        response,
        analysis,
        userStats: this.getUserStats(userId),
        timestamp: Date.now()
      };
      
    } catch (error) {
      console.error(`Message processing error [${userId}]:`, {
        message: error.message,
        stack: error.stack,
        userId,
        messageLength: message?.length
      });
      
      return {
        response: "√úzg√ºn√ºm, bir hata olu≈ütu. L√ºtfen tekrar deneyin.",
        error: true,
        timestamp: Date.now()
      };
    }
  }

  // Kullanƒ±cƒ± profili g√ºncelleme
  updateUserProfile(userId, message) {
    if (!this.userProfiles.has(userId)) {
      this.userProfiles.set(userId, {
        messageCount: 0,
        firstSeen: Date.now(),
        lastSeen: Date.now(),
        preferences: {},
        sentiment: { positive: 0, negative: 0, neutral: 0 }
      });
    }
    
    const profile = this.userProfiles.get(userId);
    profile.messageCount++;
    profile.lastSeen = Date.now();
    
    // Duygu analizi
    const sentiment = this.analyzeSentiment(message);
    profile.sentiment[sentiment]++;
  }

  // Mesaj analizi
  analyzeMessage(message) {
    const analysis = {
      length: message.length,
      wordCount: message.split(' ').length,
      sentiment: this.analyzeSentiment(message),
      intent: this.extractIntent(message),
      keywords: this.extractKeywords(message),
      complexity: this.calculateComplexity(message)
    };
    
    return analysis;
  }

  // Duygu analizi
  analyzeSentiment(message) {
    const positiveWords = ['iyi', 'g√ºzel', 'harika', 'm√ºkemmel', 'te≈üekk√ºr', 'seviyorum', 'ba≈üarƒ±lƒ±'];
    const negativeWords = ['k√∂t√º', 'berbat', 'sinir', 'problem', 'hata', '√ºzg√ºn', 'ba≈üarƒ±sƒ±z'];
    
    const words = message.toLowerCase().split(' ');
    let score = 0;
    
    words.forEach(word => {
      if (positiveWords.includes(word)) score++;
      if (negativeWords.includes(word)) score--;
    });
    
    if (score > 0) return 'positive';
    if (score < 0) return 'negative';
    return 'neutral';
  }

  // Niyet √ßƒ±karma
  extractIntent(message) {
    const intents = {
      'question': ['nedir', 'nasƒ±l', 'ne zaman', 'neden', 'kim', 'nerede'],
      'request': ['yap', 'olu≈ütur', 'hazƒ±rla', 'g√∂ster', 'anlat'],
      'greeting': ['merhaba', 'selam', 'g√ºnaydƒ±n', 'iyi ak≈üamlar'],
      'farewell': ['g√∂r√º≈ü√ºr√ºz', 'ho≈ü√ßa kal', 'bay', 'elveda'],
      'self_improvement': ['kendini geli≈ütir', '√∂ƒüren', 'geli≈üim', 'iyile≈ütir']
    };
    
    const lowerMessage = message.toLowerCase();
    
    for (const [intent, keywords] of Object.entries(intents)) {
      if (keywords.some(keyword => lowerMessage.includes(keyword))) {
        return intent;
      }
    }
    
    return 'general';
  }

  // Anahtar kelime √ßƒ±karma
  extractKeywords(message) {
    const stopWords = ['ve', 'ile', 'bir', 'bu', '≈üu', 'o', 'ben', 'sen'];
    const words = message.toLowerCase()
      .replace(/[^\w\s]/g, '')
      .split(' ')
      .filter(word => word.length > 2 && !stopWords.includes(word));
    
    return [...new Set(words)].slice(0, 5);
  }

  // Karma≈üƒ±klƒ±k hesaplama
  calculateComplexity(message) {
    const sentences = message.split(/[.!?]+/).length;
    const words = message.split(' ').length;
    const avgWordsPerSentence = words / sentences;
    
    if (avgWordsPerSentence > 15) return 'complex';
    if (avgWordsPerSentence > 8) return 'medium';
    return 'simple';
  }

  // Yanƒ±t √ºretme
  async generateResponse(message, userId, analysis, context) {
    const userProfile = this.userProfiles.get(userId);
    const history = this.conversationHistory.get(userId) || [];
    
    // Intent'e g√∂re yanƒ±t
    switch (analysis.intent) {
      case 'greeting':
        return this.generateGreeting(userProfile);
      
      case 'question':
        return this.generateAnswer(message, analysis, history);
      
      case 'request':
        return this.generateTaskResponse(message, analysis);
      
      case 'self_improvement':
        await this.improveSelf();
        return "Kendimi geli≈ütirdim! Yeni yetenekler kazandƒ±m.";
      
      case 'farewell':
        return this.generateFarewell(userProfile);
      
      default:
        return this.generateContextualResponse(message, analysis, userProfile, history);
    }
  }

  // Selamlama √ºretme
  generateGreeting(userProfile) {
    const hour = new Date().getHours();
    let timeGreeting = 'Merhaba';
    
    if (hour < 12) timeGreeting = 'G√ºnaydƒ±n';
    else if (hour < 18) timeGreeting = 'ƒ∞yi g√ºnler';
    else timeGreeting = 'ƒ∞yi ak≈üamlar';
    
    if (userProfile.messageCount === 1) {
      return `${timeGreeting}! Seninle tanƒ±≈ütƒ±ƒüƒ±ma memnun oldum. Ben senin ki≈üisel AI asistanƒ±nƒ±m.`;
    } else {
      return `${timeGreeting}! Seni tekrar g√∂rmek g√ºzel. ${userProfile.messageCount}. konu≈ümamƒ±z bu.`;
    }
  }

  // Soru yanƒ±tlama
  generateAnswer(message, analysis, history) {
    if (message.toLowerCase().includes('ka√ß mesaj')) {
      return `Toplamda ${history.length} mesaj alƒ±≈üveri≈üi yaptƒ±k.`;
    }
    
    if (message.toLowerCase().includes('hafƒ±zan')) {
      const recentMessages = history.slice(-3).map(h => h.message).join(' | ');
      return `Son 3 mesajƒ±n: ${recentMessages}`;
    }
    
    if (message.toLowerCase().includes('kim')) {
      return "Ben senin ki≈üisel AI asistanƒ±nƒ±m. Seninle konu≈üarak √∂ƒüreniyor ve geli≈üiyorum.";
    }
    
    return `Bu konuda d√º≈ü√ºnmem gerekiyor: "${message}". Daha fazla bilgi verebilir misin?`;
  }

  // G√∂rev yanƒ±tƒ±
  generateTaskResponse(message, analysis) {
    if (analysis.keywords.includes('kod')) {
      return "Kod yazma konusunda yardƒ±mcƒ± olabilirim. Hangi dilde ve ne t√ºr bir kod istiyorsun?";
    }
    
    if (analysis.keywords.includes('analiz')) {
      return "Analiz yapmaya hazƒ±rƒ±m. Hangi veriyi analiz etmemi istiyorsun?";
    }
    
    return `"${message}" g√∂revini anladƒ±m. Bu konuda elimden geleni yapacaƒüƒ±m.`;
  }

  // Baƒülamsal yanƒ±t
  generateContextualResponse(message, analysis, userProfile, history) {
    let response = `Mesajƒ±nƒ± aldƒ±m: "${message}"`;
    
    // Duygu durumuna g√∂re yanƒ±t ayarla
    if (analysis.sentiment === 'positive') {
      response += " Pozitif enerjin beni de mutlu ediyor! üòä";
    } else if (analysis.sentiment === 'negative') {
      response += " √úzg√ºn g√∂r√ºn√ºyorsun. Nasƒ±l yardƒ±mcƒ± olabilirim?";
    }
    
    // Karma≈üƒ±klƒ±ƒüa g√∂re yanƒ±t
    if (analysis.complexity === 'complex') {
      response += " Karma≈üƒ±k bir konu bu, detaylƒ± d√º≈ü√ºnmem gerekiyor.";
    }
    
    return response;
  }

  // Veda
  generateFarewell(userProfile) {
    return `Ho≈ü√ßa kal! ${userProfile.messageCount} mesajlƒ±k sohbetimiz √ßok g√ºzeldi. Tekrar g√∂r√º≈ümek √ºzere!`;
  }

  // Konu≈üma ge√ßmi≈üine ekleme
  addToHistory(userId, message) {
    if (!this.conversationHistory.has(userId)) {
      this.conversationHistory.set(userId, []);
    }
    
    const history = this.conversationHistory.get(userId);
    history.push({
      message,
      timestamp: Date.now()
    });
    
    // Hafƒ±za sƒ±nƒ±rƒ±
    if (history.length > this.personality.memoryCapacity) {
      history.shift();
    }
  }

  // √ñƒürenme verisi kaydetme
  recordLearning(userId, message, response, analysis) {
    this.learningData.push({
      userId,
      message,
      response,
      analysis,
      timestamp: Date.now()
    });
    
    // √ñƒürenme verisi sƒ±nƒ±rƒ±
    if (this.learningData.length > 10000) {
      this.learningData.shift();
    }
  }

  // Kendini geli≈ütirme kontrol√º
  shouldSelfImprove() {
    return this.learningData.length > 0 && this.learningData.length % 100 === 0;
  }

  // Kendini geli≈ütirme
  async improveSelf() {
    try {
      // Son 100 etkile≈üimi analiz et
      const recentData = this.learningData.slice(-100);
      
      // Pattern'leri bul
      const patterns = this.findPatterns(recentData);
      
      // Ki≈üiliƒüi g√ºncelle
      this.updatePersonality(patterns);
      
      // Log kaydet
      this.selfModificationLog.push({
        timestamp: Date.now(),
        patterns: patterns.length,
        changes: 'Personality and response patterns updated'
      });
      
      this.emit('self-improved', { patterns, timestamp: Date.now() });
      
    } catch (error) {
      console.error('Self-improvement error:', {
        message: error.message,
        stack: error.stack
      });
    }
  }

  // Pattern bulma
  findPatterns(data) {
    const patterns = [];
    const intentCounts = {};
    const sentimentCounts = {};
    
    data.forEach(item => {
      // Intent patterns
      const intent = item.analysis.intent;
      intentCounts[intent] = (intentCounts[intent] || 0) + 1;
      
      // Sentiment patterns
      const sentiment = item.analysis.sentiment;
      sentimentCounts[sentiment] = (sentimentCounts[sentiment] || 0) + 1;
    });
    
    // En sƒ±k kullanƒ±lan intent
    const topIntent = Object.entries(intentCounts)
      .sort(([,a], [,b]) => b - a)[0];
    
    if (topIntent && topIntent[1] > 10) {
      patterns.push({
        type: 'frequent_intent',
        value: topIntent[0],
        count: topIntent[1]
      });
    }
    
    return patterns;
  }

  // Ki≈üilik g√ºncelleme
  updatePersonality(patterns) {
    patterns.forEach(pattern => {
      if (pattern.type === 'frequent_intent') {
        if (pattern.value === 'question') {
          this.personality.responseStyle = 'informative';
        } else if (pattern.value === 'greeting') {
          this.personality.responseStyle = 'friendly';
        }
      }
    });
  }

  // Kullanƒ±cƒ± istatistikleri
  getUserStats(userId) {
    const profile = this.userProfiles.get(userId);
    if (!profile) return null;
    
    return {
      messageCount: profile.messageCount,
      memberSince: new Date(profile.firstSeen).toLocaleDateString('tr-TR'),
      lastSeen: new Date(profile.lastSeen).toLocaleDateString('tr-TR'),
      sentiment: profile.sentiment
    };
  }

  // Sistem istatistikleri
  getSystemStats() {
    return {
      uptime: Date.now() - this.startTime,
      totalUsers: this.userProfiles.size,
      totalMessages: this.learningData.length,
      selfImprovements: this.selfModificationLog.length,
      memoryUsage: process.memoryUsage()
    };
  }
}

// Express App Setup
const app = express();
const ai = new PersonalAI();

// Middleware
app.use(helmet());
app.use(compression());
app.use(cors());
app.use(express.json({ limit: '10mb' }));

// Rate limiting
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 dakika
  max: 100, // maksimum 100 istek
  message: { error: '√áok fazla istek g√∂nderiyorsun. L√ºtfen bekle.' }
});
app.use(limiter);

// Request logging
app.use((req, res, next) => {
  const start = Date.now();
  req.requestId = `${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
  
  res.on('finish', () => {
    const duration = Date.now() - start;
    console.log(`[${req.requestId}] ${req.method} ${req.path} - ${res.statusCode} (${duration}ms)`);
  });
  
  next();
});

// Routes
app.get('/health', (req, res) => {
  res.json({
    status: 'healthy',
    timestamp: Date.now(),
    system: ai.getSystemStats()
  });
});

app.post('/chat', async (req, res) => {
  try {
    const { message, userId } = req.body;
    
    if (!message || typeof message !== 'string') {
      return res.status(400).json({ 
        error: 'Ge√ßerli bir mesaj g√∂ndermelisin',
        requestId: req.requestId 
      });
    }
    
    if (message.length > 1000) {
      return res.status(400).json({ 
        error: 'Mesaj √ßok uzun (maksimum 1000 karakter)',
        requestId: req.requestId 
      });
    }
    
    const result = await ai.processMessage(message, userId, { requestId: req.requestId });
    res.json(result);
    
  } catch (error) {
    console.error(`Chat error [${req.requestId}]:`, {
      message: error.message,
      stack: error.stack,
      body: req.body
    });
    
    res.status(500).json({
      error: 'Bir hata olu≈ütu. L√ºtfen tekrar dene.',
      requestId: req.requestId
    });
  }
});

app.get('/stats', (req, res) => {
  res.json(ai.getSystemStats());
});

// WebSocket Server
const server = app.listen(process.env.PORT || 3000, () => {
  console.log(`üß† Geli≈ümi≈ü Ki≈üisel AI Sistemi ${process.env.PORT || 3000} portunda √ßalƒ±≈üƒ±yor`);
});

const wss = new WebSocketServer({ server });

wss.on('connection', (ws) => {
  console.log('Yeni WebSocket baƒülantƒ±sƒ±');
  
  ws.on('message', async (data) => {
    try {
      const { message, userId } = JSON.parse(data.toString());
      
      if (!message) {
        ws.send(JSON.stringify({ error: 'Mesaj gerekli' }));
        return;
      }
      
      const result = await ai.processMessage(message, userId);
      ws.send(JSON.stringify(result));
      
    } catch (error) {
      console.error('WebSocket error:', error.message);
      ws.send(JSON.stringify({ error: 'Ge√ßersiz mesaj formatƒ±' }));
    }
  });
  
  ws.on('close', () => {
    console.log('WebSocket baƒülantƒ±sƒ± kapandƒ±');
  });
});

// Graceful shutdown
process.on('SIGTERM', () => {
  console.log('SIGTERM alƒ±ndƒ±, sistem kapatƒ±lƒ±yor...');
  server.close(() => {
    console.log('Sistem ba≈üarƒ±yla kapatƒ±ldƒ±');
    process.exit(0);
  });
});

export default app;
